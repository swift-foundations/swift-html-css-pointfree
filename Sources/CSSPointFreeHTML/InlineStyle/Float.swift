import CSS_Standard
import PointFreeHTML

extension HTML {
    @discardableResult
    public func float(
        _ float: W3C_CSS_Positioning.Float?,
        media: W3C_CSS_MediaQueries.Media? = nil,
        selector: Selector? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(float, media: media, selector: selector, pseudo: pseudo)
    }
}
