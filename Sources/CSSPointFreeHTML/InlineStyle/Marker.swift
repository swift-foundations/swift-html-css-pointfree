import CSS_Standard
import PointFreeHTML

extension HTML {
    @discardableResult
    public func marker(
        _ marker: W3C_CSS_Images.Marker?,
        media: W3C_CSS_MediaQueries.Media? = nil,
        selector: Selector? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(marker, media: media, selector: selector, pseudo: pseudo)
    }
}
