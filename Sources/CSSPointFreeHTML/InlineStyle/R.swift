import CSS_Standard
import PointFreeHTML

extension HTML {
    @discardableResult
    public func r(
        _ r: W3C_CSS_Images.R?,
        media: W3C_CSS_MediaQueries.Media? = nil,
        selector: Selector? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(r, media: media, selector: selector, pseudo: pseudo)
    }
}
