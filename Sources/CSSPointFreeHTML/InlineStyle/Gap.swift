import CSS_Standard
import PointFreeHTML

extension HTML {
    @discardableResult
    public func gap(
        _ gap: W3C_CSS_Flexbox.Gap?,
        media: W3C_CSS_MediaQueries.Media? = nil,
        selector: Selector? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(gap, media: media, selector: selector, pseudo: pseudo)
    }
}
