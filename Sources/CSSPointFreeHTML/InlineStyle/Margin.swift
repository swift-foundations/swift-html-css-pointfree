import CSS_Standard
import PointFreeHTML

extension HTML {
    @discardableResult
    public func margin(
        _ margin: W3C_CSS_BoxModel.Margin?,
        media: W3C_CSS_MediaQueries.Media? = nil,
        selector: Selector? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(margin, media: media, selector: selector, pseudo: pseudo)
    }
}
