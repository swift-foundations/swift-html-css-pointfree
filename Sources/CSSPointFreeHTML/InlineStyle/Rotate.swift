import CSS_Standard
import PointFreeHTML

extension HTML {
    @discardableResult
    public func rotate(
        _ rotate: W3C_CSS_Transforms.Rotate?,
        media: W3C_CSS_MediaQueries.Media? = nil,
        selector: Selector? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(rotate, media: media, selector: selector, pseudo: pseudo)
    }
}
