import CSS_Standard
import PointFreeHTML

extension HTML {
    @discardableResult
    public func d(
        _ d: W3C_CSS_Images.D?,
        media: W3C_CSS_MediaQueries.Media? = nil,
        selector: Selector? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(d, media: media, selector: selector, pseudo: pseudo)
    }
}
