import CSS_Standard
import PointFreeHTML

extension HTML {
    @discardableResult
    public func tabSize(
        _ tabSize: W3C_CSS_Text.TabSize?,
        media: W3C_CSS_MediaQueries.Media? = nil,
        selector: Selector? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(tabSize, media: media, selector: selector, pseudo: pseudo)
    }
}
