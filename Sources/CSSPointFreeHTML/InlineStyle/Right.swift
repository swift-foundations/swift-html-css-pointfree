import CSS_Standard
import PointFreeHTML

extension HTML {
    @discardableResult
    public func right(
        _ right: W3C_CSS_Positioning.Right?,
        media: W3C_CSS_MediaQueries.Media? = nil,
        selector: Selector? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(right, media: media, selector: selector, pseudo: pseudo)
    }
}
