import CSS_Standard
import PointFreeHTML

extension HTML {
    @discardableResult
    public func inset(
        _ inset: W3C_CSS_Positioning.Inset?,
        media: W3C_CSS_MediaQueries.Media? = nil,
        selector: Selector? = nil,
        pseudo: Pseudo? = nil
    ) -> HTMLInlineStyle<Self> {
        self.inlineStyle(inset, media: media, selector: selector, pseudo: pseudo)
    }
}
